  document.addEventListener("DOMContentLoaded", function () {
    const root = document.querySelector("#product-app");
    if (!root || !window.Vue) {
      return;
    }

    fetch(PRODUCTS_URL)
      .then(function (response) {
        if (!response.ok) {
          throw new Error("Failed to load product data.");
        }
        return response.json();
      })
      .then(function (products) {
        mountProductApp(products);
      })
      .catch(function (error) {
        console.error(error);
      });
  });

  function mountProductApp(products) {
    const app = window.Vue.createApp({
      data: function () {
        return {
          products: products,
          category: "all",
          activeView: "grid",
          gridPage: 1,
          listPage: 1
        };
      },

      computed: {
        filteredProducts: function () {
          const currentCategory = this.category;
          if (currentCategory === "all") {
            return interleaveByCategory(this.products);
          }

          return this.products.filter(function (product) {
            return product.category === currentCategory;
          });
        },

        gridTotalPages: function () {
          return Math.max(1, Math.ceil(this.filteredProducts.length / GRID_PAGE_SIZE));
        },

        listTotalPages: function () {
          return Math.max(1, Math.ceil(this.filteredProducts.length / LIST_PAGE_SIZE));
        },

        visibleGridPages: function () {
          return getVisiblePages(this.gridPage, this.gridTotalPages);
        },

        visibleListPages: function () {
          return getVisiblePages(this.listPage, this.listTotalPages);
        },

        pagedGridProducts: function () {
          return this.paginate(this.filteredProducts, this.gridPage, GRID_PAGE_SIZE);
        },

        pagedListProducts: function () {
          return this.paginate(this.filteredProducts, this.listPage, LIST_PAGE_SIZE);
        },

        showingText: function () {
          const page = this.activeView === "list" ? this.listPage : this.gridPage;
          const pageSize = this.activeView === "list" ? LIST_PAGE_SIZE : GRID_PAGE_SIZE;
          const total = this.filteredProducts.length;

          if (!total) {
            return this.isChinesePage() ? "\u663e\u793a 0 \u6761\uff0c\u5171 0 \u6761\u7ed3\u679c" : "Showing 0 of 0 results";
          }

          const start = (page - 1) * pageSize + 1;
          const end = Math.min(page * pageSize, total);
          return this.isChinesePage()
            ? "\u663e\u793a " + start + "-" + end + " \u6761\uff0c\u5171 " + total + " \u6761\u7ed3\u679c"
            : "Showing " + start + "-" + end + " of " + total + " results";
        }
      },

      watch: {
        category: function () {
          this.resetPages();
          this.syncSelect();
        }
      },

      mounted: function () {
        this.bindCategorySelect();
        this.syncSelect();
        this.syncLanguage();
      },

      updated: function () {
        this.syncLanguage();
      },

      methods: {
        paginate: function (items, page, pageSize) {
          const totalPages = Math.max(1, Math.ceil(items.length / pageSize));
          const safePage = Math.min(page, totalPages);
          const start = (safePage - 1) * pageSize;
          return items.slice(start, start + pageSize);
        },

        resetPages: function () {
          this.gridPage = 1;
          this.listPage = 1;
        },

        setView: function (view) {
          this.activeView = view;
        },

        setCategory: function (category) {
          this.category = category || "all";
        },

        getProductUrl: function (product) {
          return "shop-details.html?id=" + encodeURIComponent(product.id);
        },

        setModalProduct: function (product, image) {
          updateProductModal(product, image);
        },

        changePage: function (view, page) {
          if (view === "list") {
            this.listPage = clamp(page, 1, this.listTotalPages);
          } else {
            this.gridPage = clamp(page, 1, this.gridTotalPages);
          }
          this.scrollToShopTop();
        },

        formatPrice: function (value) {
          return "$" + Number(value).toFixed(2);
        },

        padPage: function (page) {
          return String(page).padStart(2, "0");
        },

        scrollToShopTop: function () {
          const root = this.$el;
          const top = root.getBoundingClientRect().top + window.pageYOffset - 20;
          window.scrollTo({
            top: top,
            behavior: "smooth"
          });
        },

        isChinesePage: function () {
          return document.documentElement.lang.toLowerCase().indexOf("zh") === 0;
        },

        syncLanguage: function () {
          this.bindCategorySelect();
          this.$nextTick(function () {
            document.dispatchEvent(new CustomEvent("shop-products-rendered"));
          });
        },

        bindCategorySelect: function () {
          const vm = this;
          const select = document.querySelector("#product-category-filter");
          if (!select) {
            return;
          }

          select.addEventListener("change", function () {
            vm.setCategory(select.value);
          });

          if (window.jQuery) {
            window.jQuery(select)
              .off("change.shopFilter")
              .on("change.shopFilter", function () {
                vm.setCategory(this.value);
              });
          }
        },

        syncSelect: function () {
          this.$nextTick(function () {
            const select = document.querySelector("#product-category-filter");
            if (!select) {
              return;
            }

            select.value = this.category;
            if (window.jQuery && window.jQuery.fn && window.jQuery.fn.niceSelect) {
              window.jQuery(select).niceSelect("update");
            }
          }.bind(this));
        }
      }
    });

    app.mount("#product-app");
  }

  function clamp(value, min, max) {
    return Math.min(Math.max(value, min), max);
  }

  function updateProductModal(product, image) {
    const imageNode = document.querySelector("#product-modal-image");
    const titleNode = document.querySelector("#product-modal-title");
    const detailsNode = document.querySelector("#product-modal-details");
    if (!product || !imageNode || !titleNode || !detailsNode) {
      return;
    }

    imageNode.setAttribute("src", image || product.image);
    imageNode.setAttribute("alt", product.title);
    titleNode.textContent = product.title;
    detailsNode.innerHTML = renderProductDetails(product);
  }

  function renderProductDetails(product) {
    const detail = PRODUCT_DETAILS[product.title];
    if (!detail) {
      return [
        '<div class="gt-product-detail-group">',
        "<h5>产品介绍</h5>",
        "<p>" + escapeHtml(product.description || "暂无详细介绍，后续持续添加。") + "</p>",
        "</div>"
      ].join("");
    }

    return [
      '<div class="gt-product-detail-group">',
      "<h5>产品介绍</h5>",
      "<p>" + escapeHtml(detail.intro) + "</p>",
      "</div>",
      '<div class="gt-product-detail-group">',
      "<h5>产品特点</h5>",
      "<ul>" + detail.features.map(function (feature) {
        return "<li>" + escapeHtml(feature) + "</li>";
      }).join("") + "</ul>",
      "</div>",
      '<div class="gt-product-detail-group">',
      "<h5>适用范围</h5>",
      "<p>" + escapeHtml(detail.scope) + "</p>",
      "</div>"
    ].join("");
  }

  function escapeHtml(value) {
    return String(value)
      .replace(/&/g, "&amp;")
      .replace(/</g, "&lt;")
      .replace(/>/g, "&gt;")
      .replace(/"/g, "&quot;")
      .replace(/'/g, "&#039;");
  }

  function getVisiblePages(currentPage, totalPages) {
    const visibleCount = Math.min(MAX_VISIBLE_PAGES, totalPages);
    const half = Math.floor(visibleCount / 2);
    let start = currentPage - half;
    let end = start + visibleCount - 1;

    if (start < 1) {
      start = 1;
      end = visibleCount;
    }

    if (end > totalPages) {
      end = totalPages;
      start = Math.max(1, end - visibleCount + 1);
    }

    const pages = [];
    for (let page = start; page <= end; page += 1) {
      pages.push(page);
    }
    return pages;
  }

  function interleaveByCategory(products) {
    const grouped = CATEGORY_ORDER.reduce(function (groups, category) {
      groups[category] = [];
      return groups;
    }, {});
    const otherProducts = [];

    products.forEach(function (product) {
      if (grouped[product.category]) {
        grouped[product.category].push(product);
      } else {
        otherProducts.push(product);
      }
    });

    const result = [];
    let index = 0;
    let hasProducts = true;

    while (hasProducts) {
      hasProducts = false;
      CATEGORY_ORDER.forEach(function (category) {
        const product = grouped[category][index];
        if (product) {
          result.push(product);
          hasProducts = true;
        }
      });
      index += 1;
    }

    return result.concat(otherProducts);
  }
